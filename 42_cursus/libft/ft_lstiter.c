#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	tmp = lst;
	if (tmp != 0 && f != 0)
	{
		while (tmp != 0)
		{
			(*f)(tmp->content);
			tmp = tmp->next;
		}
	}
}
