#include "bsq.h"

void	fill_map(char **m, int **cm, int n[], char f_c)
{
	int		i;
	int		j;

	if (n[0] == 0)
		return ;
	i = n[1] - n[0];
	while (++i <= n[1])
	{
		j = n[2] - n[0];
		while (++j <= n[2])
			m[i][j] = f_c;
	}
}
