int		ft_str_is_uppercase(char *str)
{
	int		n;

	n = 0;
	while (str[n] != '\0')
	{
		if (!(str[n] >= 'A' && str[n] <= 'Z'))
			return (0);
		n++;
	}
	return (1);
}
