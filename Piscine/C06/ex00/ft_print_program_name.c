#include <unistd.h>

int		main(int argc, char *argv[])
{
	int		n;
	int		trash;

	trash = argc;
	n = 0;
	while (argv[0][n] != '\0')
	{
		write(1, &argv[0][n], 1);
		n++;
	}
	write(1, "\n", 1);
}
