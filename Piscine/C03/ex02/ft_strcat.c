char	*ft_strcat(char *dest, char *src)
{
	int		n;
	int		m;

	m = 0;
	n = 0;
	while (dest[n] != '\0')
	{
		n++;
	}
	while (src[m] != '\0')
	{
		dest[n + m] = src[m];
		m++;
	}
	dest[n + m] = '\0';
	return (dest);
}
